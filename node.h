#ifndef NODE_H
#define NODE_H

//****************************************************************************************************

template <typename TYPE>
struct Node
{
	TYPE data;
	Node<TYPE> * next;
	Node ();
	Node ( TYPE d, Node<TYPE> * n = NULL );
};

//****************************************************************************************************

template <typename TYPE>
Node<TYPE> :: Node ()
{
	data = 0;
	next = NULL;
}

//****************************************************************************************************

template <typename TYPE>
Node<TYPE> :: Node ( TYPE d, Node<TYPE> * n )
{
	data = d;
	next = n;
}

//****************************************************************************************************

#endif

